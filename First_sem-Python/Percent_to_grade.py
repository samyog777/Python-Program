percent=float(input("Enter your percentage:"))
if percent>=70:
    print("You get an A")
elif percent>=60:
    print("You get a B")
elif percent>=50:
    print("You get a C")
elif percent>=40:
    print("You get a D")
elif percent>=30:
    print("You get a E")
else:
    print("You Failed the course")
